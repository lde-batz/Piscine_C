int		ft_atoi(char *str)
{
	int x;
	int neg;
	int res;

	x = 0;
	res = 0;
	neg = 0;
	while ((str[x] >= 9 && str[x] <= 13) || str[x] == 32)
		x++;
	if (str[x] == 43 || str[x] == 45)
	{
		if (str[x] == 45)
			neg++;
		x++;
	}
	while (str[x] >= 48 && str[x] <= 57)
	{
		res = res * 10;
		res = res + (str[x] - 48);
		x++;
	}
	if (neg == 1)
		res = -res;
	return (res);
}

char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 48 && str[i] <= 57) || (str[i] >= 65 && str[i] <= 90) ||
				(str[i] >= 97 && str[i] <= 122))
		{
			if (str[i] >= 97 && str[i] <= 122)
				str[i] -= 32;
			i++;
		}
		while ((str[i] >= 48 && str[i] <= 57) || (str[i] >= 65 && str[i] <= 90)
				|| (str[i] >= 97 && str[i] <= 122))
		{
			if (str[i] >= 65 && str[i] <= 90)
				str[i] += 32;
			i++;
		}
		i++;
	}
	return (str);
}

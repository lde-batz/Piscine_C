int		match(char *s1, char *s2)
{
	if (*s2 == '\0' && *s1 == *s2)
		return (1);
	if (*s1 == *s2 && *s1 && *s2)
		return (match(s1 + 1, s2 + 1));
	if (*s1 && *s2 == '*')
	{
		if (!match(s1 + 1, s2) && !match(s1, s2 + 1))
			return (0);
		else
			return (1);
	}
	if (*s1 == '\0' && *s2 == '*')
		return (match(s1, s2 + 1));
	return (0);
}
